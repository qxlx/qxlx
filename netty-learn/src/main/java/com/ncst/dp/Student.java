public class Student {

    private String name;
    private String age;
    private String money;
    private String offer;

    public Student setName(String name) {
        this.name = name;
        return this;
    }

    public Student setAge(String age) {
        this.age = age;
        return this;
    }

    public Student setMoney(String money) {
        this.money = money;
        return this;
    }

    public Student setOffer(String offer) {
        this.offer = offer;
        return this;
    }
}
